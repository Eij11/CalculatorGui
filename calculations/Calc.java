    private void JB_equalsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JB_equalsActionPerformed
       snum = Double.parseDouble(jLabel_result.getText());
       
       if(operator == "+"){
           result = fnum + snum;
           jLabel_result.setText(String.valueOf(result));
       }
       
       else if(operator == "-"){
           result = fnum - snum;
           jLabel_result.setText(String.valueOf(result));
       }
       
       else if(operator == "x"){
           result = fnum * snum;
           jLabel_result.setText(String.valueOf(result));
       }
       
       else if(operator == "รท"){
           result = fnum / snum;
           jLabel_result.setText(String.valueOf(result));
       }
       
        else if(operator == "%"){
           result = fnum % snum;
           jLabel_result.setText(String.valueOf(result));
       }
    }//GEN-LAST:event_JB_equalsActionPerformed

